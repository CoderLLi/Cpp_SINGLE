#include<iostream>
using namespace std;
int main()
{
    int n;
    cin >> n;
    if (n<=100000)cout<<0.1*n;
    else if(100000<n&&n<=200000)cout<<10000+(n-100000)*0.075;
    else if(200000<n&&n<=400000)cout<<17500+(n-200000)*0.05;
    else if(400000<n&&n<=600000)cout<<17500+200000*0.05+(n-400000)*0.03;
    else if(600000<n&&n<=1000000)cout<<17500+200000*0.05+(600000-400000)*0.03+(n-600000)*0.015;
    else cout<<17500+200000*0.05+(600000-400000)*0.03+(1000000-600000)*0.015+(n-1000000)*0.01;
}